  public void Initialize(IncrementalGeneratorInitializationContext context)
  {
    var files = context.AdditionalTextsProvider.Where(file => Path.GetFileName(file.Path).Equals("vk.xml",StringComparison.InvariantCulture));
    context.RegisterSourceOutput(context.CompilationProvider.Combine(files.Collect()),static (context,pair) =>
    {
      if(pair.Right.Length == 0)
      {
        context.ReportDiagnostic(Diagnostic.Create(noVkXmlFileDiagnostic,null,"vk.xml"));
        return;
      }
      if(pair.Right.Length > 1)
      {
        context.ReportDiagnostic(Diagnostic.Create(multipleVkXmlFilesDiagnostic,null,"vk.xml"));
        return;
      }
      var vkDocument = new XmlDocument();
      vkDocument.Load(pair.Right[0].Path);
      var vkData = VulkanHeaderData.CreateFrom(vkDocument);
      BuildOutputFile(context,vkData,"Vulkan.g.cs");
    });
  }

