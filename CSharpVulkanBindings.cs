  public static XmlElement GetFirstElementByName(this XmlNodeList nodeList,string name)
  {
    return nodeList.OfType<XmlElement>().First(element => element.Name == name);
  }

  public static IEnumerable<XmlElement> GetAllElementsByName(this XmlNodeList nodeList,string name)
  {
    return nodeList.OfType<XmlElement>().Where(element => element.Name == name);
  }
