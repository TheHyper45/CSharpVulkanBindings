  private static StructMember ParseXmlStructMember(XmlElement element)
  {
    var typeStr = "";
    var nameStr = "";
    foreach(var child in element.ChildNodes.OfType<XmlNode>())
    {
      if(child is XmlElement childElement)
      {
        if(childElement.Name == "name")
        {
          nameStr = childElement.InnerText;
        }
        else if(childElement.Name == "type" || childElement.Name == "enum")
        {
          typeStr += childElement.InnerText;
        }
      }
      else if(child is XmlText childText)
      {
        typeStr += childText.InnerText;
      }
    }
    nameStr = nameStr.Trim();
    if(nameStr == "event") nameStr = "@event";
    if(nameStr == "object") nameStr = "@object";
    if(nameStr == "params") nameStr = "@params";

    typeStr = typeStr.Replace("const","").Replace("struct","").Replace("union","").Replace("enum","").Replace(" ","");
    var pointerIndex = typeStr.IndexOf('*');
    var leftBracketIndex = typeStr.IndexOf('[');
    var rightBracketIndex = typeStr.IndexOf(']');
    var colonIndex = typeStr.IndexOf(':');

    if(colonIndex != -1)
    {
      var targetType = typeStr.Substring(0,colonIndex);
      var bitfields = typeStr.Substring(colonIndex + 1);
      return new() { name = nameStr,type = ReplaceBaseType(targetType),bitFields = bitfields };
    }

    if(pointerIndex == -1 && leftBracketIndex == -1)
    {
      return new() { name = nameStr,type = ReplaceBaseType(typeStr) };
    }
    if(pointerIndex == -1 && leftBracketIndex != -1)
    {
      var arrayedType = typeStr.Substring(0,leftBracketIndex);
      var arraySize = typeStr.Substring(leftBracketIndex + 1,rightBracketIndex - leftBracketIndex - 1);
      return new() { name = nameStr,type = ReplaceBaseType(arrayedType),arraySize = arraySize };
    }
    if(pointerIndex != -1 && leftBracketIndex == -1)
    {
      var targetType = typeStr.Substring(0,pointerIndex);
      var asterisks = typeStr.Substring(pointerIndex);
      return new() { name = nameStr,type = ReplaceBaseType(targetType) + asterisks };
    }
    var innerType = typeStr.Substring(0,pointerIndex);
    var asterisks2 = typeStr.Substring(pointerIndex,leftBracketIndex - pointerIndex);
    var arraySize2 = typeStr.Substring(leftBracketIndex + 1,rightBracketIndex - leftBracketIndex - 1);
    return new() { name = nameStr,type = ReplaceBaseType(innerType) + asterisks2,arraySize = arraySize2 };
  }

  private static bool InvalidName(string name) => name.Contains("Video") || name.Contains("VIDEO");

  private static void ParseXmlFunction(VulkanHeaderData data,XmlElement functionElement,bool isBaseType)
  {
    var funcAliasAttr = functionElement.Attributes["alias"];
    if(funcAliasAttr != null)
    {
      var funcName = functionElement.Attributes["name"].Value;
      if(!funcName.StartsWith("PFN_")) funcName = "PFN_" + funcName;
      var funcAlias = funcAliasAttr.Value;
      if(!funcAlias.StartsWith("PFN_")) funcAlias = "PFN_" + funcAlias;
      data.typeAliases.Add(funcName,funcAlias);
      return;
    }
    var funcProto = ParseXmlStructMember(functionElement.ChildNodes.GetFirstElementByName("proto"));
    if(InvalidName(funcProto.name)) return;
    List<StructMember> funcParams = [];
    foreach(var paramNode in functionElement.ChildNodes.GetAllElementsByName("param"))
    {
      var paramApiAttr = paramNode.Attributes["api"];
      if(paramApiAttr != null && !paramApiAttr.Value.Split(',').Contains("vulkan")) continue;
      funcParams.Add(ParseXmlStructMember(paramNode));
    }
    if(!funcProto.name.StartsWith("PFN_")) funcProto.name = "PFN_" + funcProto.name;

    List<FunctionParameter> functionParameters = [.. funcParams.Select(element => new FunctionParameter() { name = element.name,type = element.type })];
    data.funcPointers[funcProto.name] = new()
    {
      isBaseType = isBaseType,
      returnType = funcProto.type,
      parameters = functionParameters,
      delegateUnmanagedString = "delegate* unmanaged<" + string.Join(",",[.. functionParameters.Select(element => element.type),funcProto.type]) + ">",
      fullArgumentsString = string.Join(",",[.. functionParameters.Select(element => element.type + " " + element.name)]),
      argumentNamesString = string.Join(",",[.. functionParameters.Select(element => element.name)])
    };
  }

  private static void ParseXmlTypes(VulkanHeaderData data,XmlElement typesElement)
  {
    foreach(var typeNode in typesElement.ChildNodes.GetAllElementsByName("type"))
    {
      var typeApiAttr = typeNode.Attributes["api"];
      if(typeApiAttr != null && !typeApiAttr.Value.Split(',').Contains("vulkan")) continue;

      var typeAlias = typeNode.Attributes["alias"];
      if(typeAlias != null)
      {
        var typeName = typeNode.Attributes["name"].Value;
        if(InvalidName(typeName)) continue;
        data.typeAliases[typeName] = typeAlias.Value;
        continue;
      }

      var typeCategoryAttr = typeNode.Attributes["category"];
      if(typeCategoryAttr == null) continue;
      if(typeCategoryAttr.Value == "handle")
      {
        var handleName = typeNode.ChildNodes.GetFirstElementByName("name").InnerText;
        if(InvalidName(handleName)) continue;
        var handleType = typeNode.ChildNodes.GetFirstElementByName("type").InnerText;
        data.handles[handleName] = handleType.Contains("NON_DISPATCHABLE");
      }
      else if(typeCategoryAttr.Value == "bitmask")
      {
        var bitmaskName = typeNode.ChildNodes.GetFirstElementByName("name").InnerText;
        if(InvalidName(bitmaskName)) continue;
        var bitmaskType = typeNode.ChildNodes.GetFirstElementByName("type").InnerText;
        data.enums[bitmaskName] = new() { isFlags = true,underlyingType = (bitmaskType == "VkFlags64" ? "long" : "int") };
      }
      else if(typeCategoryAttr.Value == "enum")
      {
        var typeName = typeNode.Attributes["name"].Value;
        if(InvalidName(typeName)) continue;
        data.enums[typeName] = new() { underlyingType = "int" };
      }
      else if(typeCategoryAttr.Value == "funcpointer")
      {
        ParseXmlFunction(data,typeNode,true);
      }
      else if(typeCategoryAttr.Value == "struct" || typeCategoryAttr.Value == "union")
      {
        var structName = typeNode.Attributes["name"].Value;
        if(InvalidName(structName)) continue;
        List<StructMember> structMembers = [];
        foreach(var memberNode in typeNode.ChildNodes.GetAllElementsByName("member"))
        {
          var memberApiAttr = memberNode.Attributes["api"];
          if(memberApiAttr != null && !memberApiAttr.Value.Split(',').Contains("vulkan")) continue;
          structMembers.Add(ParseXmlStructMember(memberNode));
        }
        data.structs[structName] = new() { isUnion = (typeCategoryAttr.Value == "union"),members = structMembers };
      }
    }
  }

  private static void ParseXmlEnums(VulkanHeaderData data,XmlElement enumsElement)
  {
    var enumsApiAttr = enumsElement.Attributes["api"];
    if(enumsApiAttr != null && !enumsApiAttr.Value.Split(',').Contains("vulkan")) return;

    var enumType = enumsElement.Attributes["type"].Value;
    if(enumType == "constants")
    {
      foreach(var constantElement in enumsElement.ChildNodes.GetAllElementsByName("enum"))
      {
        var constantType = constantElement.Attributes["type"].Value;
        var constantValue = constantElement.Attributes["value"].Value;
        var constantName = constantElement.Attributes["name"].Value;
        constantValue = constantValue.Replace("F","f").Replace("U","u").Replace("LL","l").Replace("L","").Trim('(',')');
        constantType = constantType.Replace("uint32_t","uint").Replace("uint64_t","ulong");
        data.constants.Add(constantName,new() { type = constantType,value = constantValue });
      }
    }
    else if(enumType == "enum" || enumType == "bitmask")
    {
      var enumName = enumsElement.Attributes["name"].Value;
      if(!InvalidName(enumName))
      {
        var @enum = data.enums[enumName];
        @enum.isFlags = (enumType == "bitmask");
        var enumBitwidthAttr = enumsElement.Attributes["bitwidth"];
        var enumBitwidth = 32;
        if(enumBitwidthAttr != null && enumBitwidthAttr.Value == "64")
        {
          enumBitwidth = 64;
          @enum.underlyingType = "long";
        }

        foreach(var enumElement in enumsElement.ChildNodes.GetAllElementsByName("enum"))
        {
          var enumElementName = enumElement.Attributes["name"].Value;
          if(InvalidName(enumElementName)) continue;
          var enumElementValueAttr = enumElement.Attributes["value"];
          var enumElementBitposAttr = enumElement.Attributes["bitpos"];
          var enumElementAliasAttr = enumElement.Attributes["alias"];

          if(enumElementAliasAttr != null)
          {
            @enum.members[enumElementName] = enumElementAliasAttr.Value;
          }
          else if(enumElementValueAttr != null)
          {
            @enum.members[enumElementName] = enumElementValueAttr.Value;
          }
          else if(enumElementBitposAttr != null)
          {
            if(enumBitwidth == 64)
            {
              @enum.members[enumElementName] = $"1L << {enumElementBitposAttr.Value}";
            }
            else
            {
              @enum.members[enumElementName] = $"1 << {enumElementBitposAttr.Value}";
            }
          }
        }
      }
    }
  }

  private static void ParseXmlCommands(VulkanHeaderData data,XmlElement commandsElement)
  {
    foreach(var commandElement in commandsElement.ChildNodes.GetAllElementsByName("command"))
    {
      var commandExportAttr = commandElement.Attributes["export"];
      if(commandExportAttr != null && !commandExportAttr.Value.Split(',').Contains("vulkan")) continue;
      ParseXmlFunction(data,commandElement,false);
    }
  }

  private static void ParseXmlFeature(VulkanHeaderData data,XmlElement featureElement)
  {
    var featureApiAttr = featureElement.Attributes["api"];
    if(featureApiAttr != null && !featureApiAttr.Value.Split(',').Contains("vulkan")) return;

    foreach(var requireElement in featureElement.ChildNodes.GetAllElementsByName("require"))
    {
      foreach(var enumElement in requireElement.ChildNodes.GetAllElementsByName("enum"))
      {
        var name = enumElement.Attributes["name"].Value;
        var valueAttr = enumElement.Attributes["value"];
        var extendsAttr = enumElement.Attributes["extends"];
        if(extendsAttr == null)
        {
          if(valueAttr != null)
          {
            data.constants[name] = new() { value = valueAttr.Value,type = valueAttr.Value.Contains('\"') ? "string" : "int" };
          }
          continue;
        }
        var extends = extendsAttr.Value;
        if(InvalidName(extends)) continue;

        var dirAttr = enumElement.Attributes["dir"];
        var dir = (dirAttr != null) ? dirAttr.Value : "";

        var offsetAttr = enumElement.Attributes["offset"];
        var extnumberAttr = enumElement.Attributes["extnumber"];
        var bitposAttr = enumElement.Attributes["bitpos"];
        var aliasAttr = enumElement.Attributes["alias"];

        var @enum = data.enums[extends];
        if(aliasAttr != null)
        {
          @enum.members[name] = aliasAttr.Value;
        }
        else if(extnumberAttr != null)
        {
          @enum.members[name] = $"{dir}1000{int.Parse(extnumberAttr.Value) - 1:D3}{int.Parse(offsetAttr.Value):D3}";
        }
        else if(valueAttr != null)
        {
          @enum.members[name] = valueAttr.Value;
        }
        else if(bitposAttr != null)
        {
          if(@enum.underlyingType == "long")
          {
            @enum.members[name] = $"1L << {bitposAttr.Value}";
          }
          else
          {
            @enum.members[name] = $"1 << {bitposAttr.Value}";
          }
        }
      }
    }
  }

  private static void ParseXmlExtensions(VulkanHeaderData data,XmlElement extensionsElement)
  {
    foreach(var extensionElement in extensionsElement.ChildNodes.GetAllElementsByName("extension"))
    {
      var featureSupportedAttr = extensionElement.Attributes["supported"];
      if(featureSupportedAttr != null && !featureSupportedAttr.Value.Split(',').Contains("vulkan")) continue;

      var extensionNumberAttr = extensionElement.Attributes["number"];

      foreach(var requireElement in extensionElement.ChildNodes.GetAllElementsByName("require"))
      {
        foreach(var enumElement in requireElement.ChildNodes.GetAllElementsByName("enum"))
        {
          var name = enumElement.Attributes["name"].Value;
          var extendsAttr = enumElement.Attributes["extends"];
          var offsetAttr = enumElement.Attributes["offset"];
          var bitposAttr = enumElement.Attributes["bitpos"];
          var valueAttr = enumElement.Attributes["value"];
          var aliasAttr = enumElement.Attributes["alias"];

          if(extendsAttr == null)
          {
            if(valueAttr != null)
            {
              data.constants[name] = new() { value = valueAttr.Value,type = valueAttr.Value.Contains('\"') ? "string" : "int" };
            }
            continue;
          }
          var extends = extendsAttr.Value;
          if(InvalidName(extends)) continue;

          var dirAttr = enumElement.Attributes["dir"];
          var dir = (dirAttr != null) ? dirAttr.Value : "";
          var @enum = data.enums[extends];
          if(aliasAttr != null)
          {
            @enum.members[name] = aliasAttr.Value;
          }
          else if(offsetAttr != null)
          {
            @enum.members[name] = $"{dir}1000{int.Parse(extensionNumberAttr.Value) - 1:D3}{int.Parse(offsetAttr.Value):D3}";
          }
          else if(valueAttr != null)
          {
            @enum.members[name] = valueAttr.Value;
          }
          else if(bitposAttr != null)
          {
            if(@enum.underlyingType == "long")
            {
              @enum.members[name] = $"1L << {bitposAttr.Value}";
            }
            else
            {
              @enum.members[name] = $"1 << {bitposAttr.Value}";
            }
          }
        }
      }
    }
  }

  public static void ParseXmlFormats(VulkanHeaderData data,XmlElement formatsElement)
  {
    foreach(var formatElement in formatsElement.ChildNodes.GetAllElementsByName("format"))
    {
      var nameValue = formatElement.Attributes["name"].Value;
      var classValue = formatElement.Attributes["class"].Value.TrimStart();
      if(classValue.StartsWith("8-bit"))
      {
        data.formatSizes.Add(nameValue,1);
      }
      else if(classValue.StartsWith("16-bit"))
      {
        data.formatSizes.Add(nameValue,2);
      }
      else if(classValue.StartsWith("24-bit"))
      {
        data.formatSizes.Add(nameValue,3);
      }
      else if(classValue.StartsWith("32-bit"))
      {
        data.formatSizes.Add(nameValue,4);
      }
      else if(classValue.StartsWith("48-bit"))
      {
        data.formatSizes.Add(nameValue,6);
      }
      else if(classValue.StartsWith("64-bit"))
      {
        data.formatSizes.Add(nameValue,8);
      }
      else if(classValue.StartsWith("96-bit"))
      {
        data.formatSizes.Add(nameValue,12);
      }
      else if(classValue.StartsWith("128-bit"))
      {
        data.formatSizes.Add(nameValue,16);
      }
      else if(classValue.StartsWith("192-bit"))
      {
        data.formatSizes.Add(nameValue,24);
      }
      else if(classValue.StartsWith("256-bit"))
      {
        data.formatSizes.Add(nameValue,32);
      }
    }
  }

  public static VulkanHeaderData CreateFrom(XmlDocument xmlDocument)
  {
    VulkanHeaderData data = new();
    foreach(var child in xmlDocument.DocumentElement.ChildNodes.OfType<XmlElement>())
    {
      if(child.Name == "types")
      {
        ParseXmlTypes(data,child);
      }
      else if(child.Name == "enums")
      {
        ParseXmlEnums(data,child);
      }
      else if(child.Name == "commands")
      {
        ParseXmlCommands(data,child);
      }
      else if(child.Name == "feature")
      {
        ParseXmlFeature(data,child);
      }
      else if(child.Name == "extensions")
      {
        ParseXmlExtensions(data,child);
      }
      else if(child.Name == "formats")
      {
        ParseXmlFormats(data,child);
      }
    }
    return data;
  }
}

file static class CollectionsExtensions
